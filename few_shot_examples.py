def build_few_shot_block() -> str:
    """
    Собирает готовый текстовый блок с эталонными примерами писем для вставки
    в промпт main.py при генерации нового письма.

    Возвращает пустую строку, если примеров нет (тогда промпт работает
    без few-shot части).
    """
    if not FEW_SHOT_EXAMPLES:
        return ""

    lines = []
    lines.append("━━━ ЭТАЛОННЫЕ ПРИМЕРЫ ПИСЕМ ━━━")
    lines.append("")
    lines.append(
        "Ниже — примеры реальных сопроводительных писем, написанных вручную "
        "под конкретные вакансии по тем же правилам, что применяются к этому "
        "письму. Изучи их: не для копирования структуры, а для понимания "
        "тона, ритма, глубины конкретики и способа обыгрывания gap-ов между "
        "кандидатом и требованиями вакансии."
    )
    lines.append("")
    lines.append("Каждый пример состоит из трёх блоков:")
    lines.append("• ВАКАНСИЯ — краткое описание вакансии и её ключевого фокуса")
    lines.append("• СТРАТЕГИЯ — какие ходы работают в этом типе вакансии")
    lines.append("• ПИСЬМО — полный текст письма-эталона")
    lines.append("")
    lines.append("Обрати особое внимание на:")
    lines.append("— как открываются письма (прямая цитата вакансии или конкретный факт, а не общие фразы)")
    lines.append("— как обыгрываются gap-ы (честная траектория / переворот в уникальный угол — не хеджирование и не преувеличение)")
    lines.append("— как экономика/метрики поднимаются самостоятельно, если тема релевантна")
    lines.append("— как строится CTA (активный, с конкретным вопросом или предложением тестового)")
    lines.append("— как выполняются явные требования работодателя (портфолио, формат ответа)")
    lines.append("")

    for i, example in enumerate(FEW_SHOT_EXAMPLES, start=1):
        lines.append(f"━━━ ПРИМЕР {i} ━━━")
        lines.append("")
        lines.append(f"ВАКАНСИЯ: {example['vacancy_summary']}")
        lines.append("")
        lines.append(f"СТРАТЕГИЯ: {example['strategy_note']}")
        lines.append("")
        lines.append("ПИСЬМО:")
        lines.append(example["letter"])
        lines.append("")

    lines.append("━━━ КОНЕЦ ЭТАЛОННЫХ ПРИМЕРОВ ━━━")
    lines.append("")
    lines.append(
        "Теперь напиши письмо для новой вакансии, следуя стилю и стратегиям, "
        "которые ты увидел в примерах выше. Не копируй структуру дословно — "
        "адаптируй её под контекст новой вакансии и профиль кандидата из "
        "резюме и досье. Уровень конкретики, тон, длина абзацев, качество "
        "CTA — как в примерах."
    )
    lines.append("")

    return "\n".join(lines)



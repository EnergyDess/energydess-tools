# -*- coding: utf-8 -*-
"""Эталонные примеры сильных сопроводительных писем для few-shot промпта.

Как добавить новый пример:
  1. Добавь dict в список COVER_LETTER_EXAMPLES.
  2. Поля: job_summary (1-2 предложения о вакансии), letter_text (текст письма).
  3. Промпт пересобирается при каждом запросе — перезапускать сервер не нужно.

Как удалить пример — просто убери dict из списка.
"""

COVER_LETTER_EXAMPLES = [
    {
        "job_summary": "PLACEHOLDER — краткое описание вакансии 1 (например: AI Video Creator в международную продакшн-студию, основные задачи — генерация сцен в Kling/Runway, озвучка через ElevenLabs, монтаж).",
        "letter_text": "PLACEHOLDER — здесь будет текст сильного письма 1.\n\nЗдравствуйте!\n\n[Конкретный факт-заход, не «меня заинтересовала вакансия»]\n\n[Второй абзац: конкретика, цифры, проекты]\n\n[Финал: портфолио-ссылка если релевантна + конкретный CTA]\n\nС уважением, [Имя]",
    },
    {
        "job_summary": "PLACEHOLDER — краткое описание вакансии 2 (например: Python-разработчик в AI-стартап, FastAPI + LLM-интеграции, небольшая команда).",
        "letter_text": "PLACEHOLDER — здесь будет текст сильного письма 2.\n\nЗдравствуйте!\n\n[Конкретный факт-заход]\n\n[Конкретика и факты]\n\n[CTA]\n\nС уважением, [Имя]",
    },
    {
        "job_summary": "PLACEHOLDER — краткое описание вакансии 3 (например: контент-менеджер / редактор в медиапроект, создание текстов и работа с нейросетями).",
        "letter_text": "PLACEHOLDER — здесь будет текст сильного письма 3.\n\nЗдравствуйте!\n\n[Конкретный факт-заход]\n\n[Конкретика и факты]\n\n[CTA]\n\nС уважением, [Имя]",
    },
]


def build_few_shot_block() -> str:
    """Собирает блок few-shot примеров для вставки в промпт генерации."""
    examples = [e for e in COVER_LETTER_EXAMPLES if "PLACEHOLDER" not in e["letter_text"]]
    if not examples:
        return ""

    lines = ["━━━ ПРИМЕРЫ СИЛЬНЫХ ПИСЕМ (для ориентира стиля, не копировать дословно) ━━━\n"]
    for i, ex in enumerate(examples, 1):
        lines.append(f"ПРИМЕР {i}")
        lines.append(f"Вакансия: {ex['job_summary']}")
        lines.append(f"Письмо:\n{ex['letter_text']}")
        lines.append("")
    lines.append(
        "Ориентируйся на эти образцы по: конкретности вступления, использованию фактов вместо намерений, "
        "избирательному прикреплению портфолио, конкретному CTA в финале. "
        "Не копируй дословно, не переноси факты из этих писем в своё письмо — используй только как ориентир по стилю.\n"
    )
    return "\n".join(lines)
